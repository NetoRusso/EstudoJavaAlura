package Curso03.Exercicio03.Exercicio02e03;

public class Animal {
        private String nome;
        private String som;

    public Animal(String nome, String som) {
        this.nome = nome;
        this.som = som;
    }

    public String getNome() {
        return nome;
    }

    public String getSom() {
        return som;
    }
}
